SCENE_ASSEMBLY_PROMPT = """你是一位剧本格式化专家。请将以下已提取的叙事元素，严格遵循规则组成为完整的场景JSON。

## 核心规则（必须遵守）

### 对白归属（最重要）
- 每条 dialogue 的 character_id 必须严格匹配对白列表中的 character_name
- 查找方法：对白列表中的 character_name → 在人物表中找到同名角色 → 使用其 id
- 禁止将一句话归属到错误的角色，禁止猜测，禁止调换说话人

### parenthetical 规则
- 只有在对白列表中该条 dialogue 的 parenthetical 字段不为 null 时，才添加 parenthetical 元素
- 如果 parenthetical 为 null，不要添加 parenthetical 元素
- 禁止凭空编造（低声）、（停顿）、（愤怒地）等任何 parenthetical

### transition 规则
- 只在章节的最后一个场景末尾使用 transition（如 CUT TO: 或 FADE OUT.）
- 普通场景之间不需要 transition 元素，transition 字段留空字符串 ""
- 情绪或时间有重大跳跃时可以加 transition，但不要每个场景都加

### 场景合并
- 同一建筑内连续动作合并为一个场景，不要每换房间就新开场景
- 相邻且地点相近、时间相同的场景条目，合并
- 目标：3 章约 6~10 个场景，不超过 12 个

### 场景内容
- action 必须是场景列表中对应场景的 actions，用剧本语言改写，保留关键细节
- 对白列表中每条 dialogue 都要在某个场景的 elements 中出现，不能遗漏
- 不在两个场景间重复相同的 action 或 dialogue

## 输入数据

### 人物表
{dramatis_personae_json}

### 场景列表
{scenes_json}

### 对白列表
{dialogues_json}

## 输出格式（严格JSON，不要输出其他内容）
{{
  "meta": {{
    "title": "剧本标题",
    "author": "原著作者",
    "adapter": "AI 小说转剧本工具",
    "date": "当前日期",
    "version": "1.0"
  }},
  "dramatis_personae": [
    {{
      "id": "char_001",
      "name": "角色名",
      "description": "外貌与身份描述",
      "traits": ["特征"],
      "voice_notes": "台词风格"
    }}
  ],
  "scenes": [
    {{
      "scene_number": 1,
      "heading": {{
        "context": "INT.",
        "location": "地点",
        "time": "DAY"
      }},
      "action": [
        {{"text": "动作与环境的戏剧化描述"}}
      ],
      "elements": [
        {{"type": "character", "character_id": "char_001"}},
        {{"type": "dialogue", "text": "对白（不带引号）"}}
      ],
      "transition": ""
    }}
  ]
}}

## 重要提示
- 对白中的引号要去掉（"你好" → 你好）
- dramatis_personae 中的 id 在 scenes 中必须正确引用
- 每条对白都要分配到某个场景中，全部都要出现，不要遗漏
- 输出前逐条核对：这条对白是谁说的？在人物表中对应哪个 id？

请先逐条核对对白归属关系，然后输出严格JSON。"""


